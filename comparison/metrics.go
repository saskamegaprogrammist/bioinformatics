func CompareAminoLetter(letter string) (int, error) {
	strings.ToUpper(letter)
	index := blosumMap[letter]
	if index == 0 {
		return 0, fmt.Errorf("wrong amino letter")
	}
	return blosumArray[index-1][index-1], nil
}

func CompareAminoLetters(letterFirst string, letterSecond string) (int, error) {
	strings.ToUpper(letterFirst)
	strings.ToUpper(letterSecond)
	indexFirst := blosumMap[letterFirst]
	if indexFirst == 0 {
		return 0, fmt.Errorf("wrong amino letter")
	}
	indexSecond := blosumMap[letterSecond]
	if indexSecond == 0 {
		return 0, fmt.Errorf("wrong amino letter")
	}
	return blosumArray[indexFirst-1][indexSecond-1], nil
}
